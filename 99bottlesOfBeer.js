numBeer = 99;

while (numBeer > 1){
    console.log(`${numBeer} bottles of beer on the wall, ${numBeer} bottles of beer. \n You take one down and pass it around, ${numBeer-1} more bottles of beer on the wall.`)
    numBeer --;
}

console.log(`${numBeer} bottle of beer on the wall, ${numBeer} bottle of beer. \n You take one down and pass it around, no more bottles of beer on the wall.`);